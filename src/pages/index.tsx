import type { NextPage } from 'next';
import { signIn, signOut, useSession } from 'next-auth/react';
import Head from 'next/head';
import { useState } from 'react';
import UpcomingTestsSection from '../components/UpcomingTestsSection';
import { trpc } from '../utils/trpc';

const Home: NextPage = () => {
  const { data, status } = useSession();

  const { data: placementEvents, isLoading } = trpc.useQuery([
    'placement.get-placement-upcoming-events',
  ]);

  const [selectedTab, setSelectedTab] = useState<'EXPERIENCES' | 'TESTS'>(
    'EXPERIENCES'
  );

  if (isLoading) {
    return <div>...</div>;
  }

  if (status === 'loading') {
    return <div>'...Loading'</div>;
  } else if (status === 'unauthenticated') {
    return (
      <div>
        <button onClick={() => signIn()}>Sign In</button>
      </div>
    );
  }

  const name = data?.user?.name;

  return (
    <>
      <Head>
        <title>PESU Placement Information Tool</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <div className="drawer drawer-mobile">
        <input id="my-drawer-2" type="checkbox" className="drawer-toggle" />
        <div className="drawer-content">
          <label
            htmlFor="my-drawer-2"
            className="btn btn-primary drawer-button lg:hidden"
          >
            Open drawer
          </label>
          {selectedTab === 'EXPERIENCES' ? (
            <>
              <h1 className="text-3xl text-center font-bold">
                Welcome {name}!
              </h1>
              <button onClick={() => signOut()}>Sign Out</button>
            </>
          ) : (
            <UpcomingTestsSection placementEvents={placementEvents} />
          )}
        </div>
        <div className="drawer-side">
          <label htmlFor="my-drawer-2" className="drawer-overlay"></label>
          <ul className="menu p-4 overflow-y-auto w-80 bg-base-100 text-base-content">
            <li onClick={() => setSelectedTab('EXPERIENCES')}>
              <a>Placement Expiriences</a>
            </li>
            <li onClick={() => setSelectedTab('TESTS')}>
              <a>Upcoming Placement Tests</a>
            </li>
          </ul>
        </div>
      </div>
    </>
  );
};

export default Home;
